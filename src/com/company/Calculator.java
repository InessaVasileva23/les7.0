package com.company;
public class Calculator {
    //Вычитание
    public double Subtraction (double a,double b) {
        return (a-b);
    }

    //Сложение
    public double adition (double a,double b) {
        return (a+b);
    }

    //Умножение
    public double multiplicatior (double a,double b) {
        return (a*b);
    }

    //Деление
    public double division (double a,double b) {
        return (a/b);
    }

    //вычисление процента от числа
    //a - число
    //b - процент
    public double CetPercentFromnumber(double a,double b) {
        return (a*b/100);
    }
}

